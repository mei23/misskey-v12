<template>
<button class="zuvgdzyu _button" @click="menu">
	<img :src="emoji.url" class="img" :alt="emoji.name"/>
	<div class="body">
		<div class="name _monospace">{{ emoji.name }}</div>
		<div class="info">{{ emoji.aliases.join(' ') }}</div>
	</div>
</button>
</template>

<script lang="ts">
import { defineComponent } from 'vue';
import * as os from '@client/os';
import copyToClipboard from '@client/scripts/copy-to-clipboard';
import VanillaTilt from 'vanilla-tilt';
import { url } from '@client/config';

export default defineComponent({
	props: {
		emoji: {
			type: Object,
			required: true,
		}
	},

	mounted() {
		if (this.$store.animation) {
			VanillaTilt.init(this.$el, {
				reverse: true,
				gyroscope: false,
				scale: 1.1,
				speed: 500,
			});
		}
	},

	methods: {
		menu(ev) {
			os.popupMenu([{
				type: 'label',
				text: ':' + this.emoji.name + ':',
			}, {
				text: this.$ts.copy,
				icon: 'fas fa-copy',
				action: () => {
					copyToClipboard(`:${this.emoji.name}:`);
					os.success();
				}
			}, {
				text: 'リモートに飛ばす',
				icon: 'fas fa-copy',
				action: async () => {
					const { canceled, result: acct } = await os.dialog({
						title: 'アカウント入れて',
						input: {
							placeholder: 'user@example.com',
						},
					});

					if (canceled) return;

					const res = await os.api('ap/interact' as any, {
						acct
					});

					const template = res.template as string;
					console.log(template.replace('{uri}', `${url}/emojis/${this.emoji.name}`));
					window.open(template.replace('{uri}', `${url}/emojis/${this.emoji.name}`), '_blank');

					os.success();
				}
			}], ev.currentTarget || ev.target);
		}
	}
});
</script>

